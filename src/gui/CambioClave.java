    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String anterior = Arrays.toString(this.psw_antigua.getPassword());
        String nueva = Arrays.toString(this.psw_nueva.getPassword());
        Usuario u = cliente.Cliente.conectarU();
        boolean validar ;
        try {
            validar = u.verificarClave(anterior, id, tipo);

            if (validar) {

                if (u.cambiarClave(nueva, id, tipo)) {
                    JOptionPane.showMessageDialog(null, "La clave fue cambiada exitosamente");
                }
            } else {
                JOptionPane.showMessageDialog(null, "La clave anterior es incorrecta");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(CambioClave.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

